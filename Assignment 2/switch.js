let day = "Wednesday";
console.log("Day = " + day);
switch (day) {
  case "Monday":
    console.log("Study-Javascript");
    break;
  case "Tuesday":
    console.log("Study-Java");
    break;
  case "Wednesday":
    console.log("Study-HTML");
    break;
  case "Thursday":
    console.log("Study-PHP");
    break;
  case "Friday":
    console.log("Study-C-Language");
    break;
  case "Saturday":
    console.log("Study-C#");
    break;
  case "Sunday":
    console.log("Study-Python");
    break;
  default:
    console.log("Wrong Input");
}
