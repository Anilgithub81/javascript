// // A JavaScript Set is a collection of unique values.
// // A Set can hold any value of any data type.
// // it creates as in object form.

// To create a new set.
const num = new Set([1,2,3,4,5]);
console.log("Result :- ",num);


// To add new element in set 

num.add(7);
console.log("Result :- ", num);

